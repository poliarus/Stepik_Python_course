students = ['Ivan', 'Masha', 'Sasha']

for student in students:
    print("Hello " + student + "!")

# Обращение к элементам списка по индексу
print(students[0])
print(students[1])
print(students[2])
print(students[-1])
print(students[-2])
print(students[-3])

# Получение среза
print(students[:2])

# Срез в обратном порядке
print(students[::-1])

print(len(students))
