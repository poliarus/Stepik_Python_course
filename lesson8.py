# Изменение окончание в зависимости от числа

for n in range(0, 1000):
    if n % 10 == 1:
        if 10 < n % 100 < 14:
            print(n, "программистов")
        else:
            print(n, "программист")
    elif 1 < n % 10 <= 4:
        if 10 < n % 100 <= 14:
            print(n, "программистов")
        else:
            print(n, "программиста")
    else:
        print(n, "программистов")
print("end")

